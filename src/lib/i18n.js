export function t(lang, key) {
  return messages[lang]?.[key] || messages.zh[key] || key;
}

export function localDateTime(value, lang) {
  if (!value) return "";
  return new Intl.DateTimeFormat(lang === "en" ? "en-US" : "zh-CN", {
    month: "short",
    day: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  }).format(new Date(value));
}
